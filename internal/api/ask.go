// ask answers a plain-English question about the data with zero dependencies,
// it routes common questions (conversion, retention, signups, channels, active
// users, the weekly brief) deterministically, right in the dashboard, no model
// required. For arbitrary questions the user connects smolanalytics to their
// OWN Claude / Cursor over MCP (we never call a model ourselves), see internal/mcp.
func (s *Server) ask(w http.ResponseWriter, r *http.Request) {
	body, _ := io.ReadAll(io.LimitReader(r.Body, 1<<20))
	var req struct {
		Question string `json:"question"`
	}
	_ = json.Unmarshal(body, &req)
	q := strings.ToLower(strings.TrimSpace(req.Question))
	if q == "" {
		writeErr(w, http.StatusBadRequest, "ask a question")
		return
	}
	evs, err := s.store.Range(time.Time{}, time.Time{})
	if err != nil {
		writeErr(w, http.StatusInternalServerError, err.Error())
		return
	}
	evs = query.Apply(evs, nil) // production scope: dev-env events excluded by default

	now := time.Now().UTC()
	// every answer ships with its receipt: which deterministic report produced it, over
	// what window, and the guarantee that a CI test proves it can't be fabricated. "Plain
	// English analytics" makes people fear hallucinated numbers — show the work so they can
	// verify before they trust. The intent is the deterministic route the classifier picked,
	// exposed so a UI can render the matching chart under the answer (reports-as-answers)
	// without re-classifying the question and drifting from the engine.
	// a refusal (unsupported time window) must return NO intent: the UIs chart by
	// intent, and a chart under "I can't answer that" reads as an answer anyway —
	// the exact incoherence the proof chip exists to prevent.
	intent := string(classifyAsk(q))
	if _, unsupported := parseWindow(q, now); unsupported != "" {
		intent = "" // a refused window renders no chart (would sit under "I can't answer")
	}
	// a question that resolved to a SPECIFIC named event (e.g. "purchases" -> checkout)
	// must not chart the classified intent's default series — better no chart than a
	// signups line under a checkout answer. webdim/live/geo are list answers with no
	// trend chart, so they render nothing anyway.
	if ev := namedEvent(q, eventsByVolume(query.Apply(evs, nil))); ev != "" && countish(q) {
		intent = ""
	}
	writeJSON(w, http.StatusOK, map[string]string{
		"answer":      answer(q, evs, now),
		"computed_by": computedBy(q, evs, now),
		"intent":      intent,
	})
}

// computedBy is the "show your work" receipt for an answer: the exact deterministic
// report + scope behind it, plus the trust guarantee. Read-only, so a founder can sanity
// check the method before trusting the number.
func computedBy(q string, evs []event.Event, now time.Time) string {
	if isAction(q) {
		return "" // action asks aren't data answers
	}
	win, _ := parseWindow(q, now)
	scope := "all recorded events"
	if win.scoped() && win.label != "" {
		scope = strings.TrimSpace(win.label)
	}
	receipt := func(report string) string {
		return "Computed by " + report + " over " + scope + ", the same engine the dashboard runs, not generated by a model. A CI test asserts this number equals the dashboard's report, so it cannot be fabricated. Open that report on the dashboard to verify."
	}
	intent := classifyAsk(q)
	// The segment / period-comparison layer (ask_scope.go) intercepts a question REGARDLESS
	// of the coarse intent — "how is reddit doing?" answers as a segment, not the verdict.
	// The receipt must describe the route ACTUALLY taken, or it names a different report than
	// the one that produced the number (a self-contradiction the receipt exists to prevent).
	// Mirror answer()'s precedence: measure and explicit-trend win before the segment layer.
	if _, _, cok := parseCompare(q, now); cok {
		return receipt("the period-comparison report (the same metric across two windows)")
	}
	// mirror answer()'s pre-intent "break down <event> by <property>" branch — without this
	// the receipt re-classified the question as unknown and told the user "no matching
	// event... I showed the weekly pulse instead" directly under a correct breakdown answer.
	if intent != intentConvBy && intent != intentRetention && intent != intentFunnel &&
		intent != intentMeasure && intent != intentPaths && intent != intentLifecycle &&
		intent != intentStickiness && intent != intentEngagement {
		if prop := breakdownByProp(q, evs); prop != "" {
			if ev := namedEvent(normalizeEventWords(q), eventsByVolume(evs)); ev != "" {
				for _, e := range evs {
					if e.Name == ev {
						if _, ok := e.Properties[prop]; ok {
							return receipt(fmt.Sprintf("the property-breakdown report (%q events per %q value)", ev, prop))
						}
					}
				}
			}
		}
	}
	if intent != intentMeasure && intent != intentConvBy && intent != intentSplit &&
		!hasAny(q, "trend", "over time", "trajectory") {
		if segs := extractSegments(q, evs); len(segs) >= 1 {
			if len(segs) >= 2 {
				return receipt("the segment-vs-segment report (each named segment, first-touch attributed)")
			}
			switch intent {
			case intentGeo, intentWebDim, intentFunnel, intentRetention, intentEngagement,
				intentHours, intentTopPages, intentEntryPages, intentAI, intentPeakDay,
				intentStickiness, intentLifecycle:
				// the segment merely FILTERS a breakdown report — the intent switch below
				// already describes it; only note the scoping.
				return receipt(reportForIntent(intent) + " (scoped to the named segment, first-touch attributed)")
			default:
				return receipt("the segment report (the named segment vs the rest, first-touch attributed)")
			}
		}
	}
	if intent == intentUnknown {
		return "You asked about something with no matching event, so nothing was fabricated. I showed the weekly pulse instead, computed deterministically. Send that metric as an event and I'll report it."
	}
	return receipt(reportForIntent(intent))
}

// reportForIntent names the deterministic report behind each intent, for the receipt.
func reportForIntent(intent askIntent) string {
	var report string
	switch intent {
	case intentBrief:
		report = "the verdict engine (notable-change detection + the weekly pulse)"
	case intentFunnel:
		report = "the funnel report"
	case intentChannels:
		report = "the first-touch channel-attribution report"
	case intentRetention:
		report = "the retention report"
	case intentActive:
		report = "the active-users report"
	case intentSignups:
		report = "the event-trend report"
	case intentWeb:
		report = "the web-overview report"
	case intentTopPages:
		report = "the top-pages report"
	case intentGeo:
		report = "the geo (countries) breakdown of the web-overview report"
	case intentEngagement:
		report = "the engagement report (visible+focused time measured by the SDK)"
	case intentMeasure:
		report = "the numeric-aggregation report (a deterministic sum/avg/median/p90 over a property you sent)"
	case intentSources:
		report = "the traffic-by-source ranking (referrer breakdown of the web-overview report, direct included)"
	case intentStickiness:
		report = "the stickiness report (DAU/MAU over exact distinct users)"
	case intentPaths:
		report = "the paths (user-journey) report — the ranked next events after the anchor"
	case intentLifecycle:
		report = "the lifecycle report (new vs returning vs dormant, exact distinct users)"
	case intentHours:
		report = "the activity-by-hour histogram"
	case intentEntryPages:
		report = "the entry-pages report (each user's first pageview in the window)"
	case intentPeakDay:
		report = "the daily event-trend report (peak bucket)"
	case intentSplit:
		report = "the referrer/utm split of the web-overview report"
	case intentAI:
		report = "the AI-referrers breakdown of the web-overview report"
	case intentConvBy:
		report = "the conversion-by-segment report (distinct users per bucket)"
	default:
		report = "a deterministic report"
	}
	return report
}

