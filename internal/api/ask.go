// answerEngagement reports engaged visitors + average engaged time over the window,
// from the SDK's $engagement events (visible AND focused time — background tabs
// don't count). The metric evaluators actually ask for by name.
func answerEngagement(evs []event.Event, win askWindow) string {
	perUser := map[string]float64{}
	for _, e := range evs {
		if e.Name != "$engagement" {
			continue
		}
		if ms, ok := e.Properties["engaged_ms"].(float64); ok && ms > 0 {
			perUser[e.DistinctID] += ms
		}
	}
	if len(perUser) == 0 {
		if win.scoped() {
			return "No engagement measured " + win.label + ". Engagement comes from the SDK's $engagement events (visible + focused time) — widen the window, or drop the time phrase for all history."
		}
		return "No engagement data yet. The browser SDK measures it automatically once installed (visible + focused time per visitor)."
	}
	var total float64
	for _, ms := range perUser {
		total += ms
	}
	avgSecs := int(total / float64(len(perUser)) / 1000)
	human := fmt.Sprintf("%ds", avgSecs)
	if avgSecs >= 60 {
		human = fmt.Sprintf("%dm %ds", avgSecs/60, avgSecs%60)
	}
	scope := "across all recorded history"
	if win.scoped() {
		scope = win.label
	}
	return fmt.Sprintf("%d engaged visitors %s, averaging %s of active time each (visible and focused — background tabs don't count).", len(perUser), scope, human)
}

func answerWebDim(evs []event.Event, q string, win askWindow) string {
	pick := func(prop string) []struct {
		v string
		n int
	} {
		// count VISITORS first-touch (each visitor's earliest pageview, once), IDENTICAL
		// to web.Compute — counting raw pageviews made the ask split (6/3) disagree with
		// the dashboard/GET/MCP visitor split (5/2) for the same question.
		firstPV := map[string]event.Event{}
		for _, e := range evs {
			if e.Name != "$pageview" {
				continue
			}
			if f, ok := firstPV[e.DistinctID]; !ok || e.Timestamp.Before(f.Timestamp) {
				firstPV[e.DistinctID] = e
			}
		}
		counts := map[string]int{}
		for _, e := range firstPV {
			if val, _ := e.Properties[prop].(string); val != "" {
				counts[val]++
			}
		}
		type kv struct {
			v string
			n int
		}
		rows := make([]kv, 0, len(counts))
		for v, n := range counts {
			rows = append(rows, kv{v, n})
		}
		sort.Slice(rows, func(i, j int) bool { return rows[i].n > rows[j].n })
		out := make([]struct {
			v string
			n int
		}, 0, len(rows))
		for i, r := range rows {
			if i == 5 {
				break
			}
			out = append(out, struct {
				v string
				n int
			}{r.v, r.n})
		}
		return out
	}
	label, prop := "devices", "device"
	switch {
	case strings.Contains(q, "browser"):
		label, prop = "browsers", "browser"
	case strings.Contains(q, "os"), strings.Contains(q, "operating system"):
		label, prop = "operating systems", "os"
	case strings.Contains(q, "bounce"):
		// bounce rate: one-pageview visitors who engaged under 10s (the web report's rule)
		pv := map[string]int{}
		eng := map[string]float64{}
		for _, e := range evs {
			if e.Name == "$pageview" {
				pv[e.DistinctID]++
			} else if e.Name == "$engagement" {
				if ms, _ := e.Properties["engaged_ms"].(float64); ms > 0 {
					eng[e.DistinctID] += ms
				}
			}
		}
		if len(pv) == 0 {
			return "No pageviews" + winSuffix(win) + " to measure bounce rate."
		}
		bounced := 0
		for u, n := range pv {
			if n == 1 && eng[u] < 10000 {
				bounced++
			}
		}
		return fmt.Sprintf("Bounce rate is %d%% (%d of %d visitors left after one page in under 10s)%s.", int(float64(bounced)/float64(len(pv))*100+0.5), bounced, len(pv), winSuffix(win))
	}
	rows := pick(prop)
	if len(rows) == 0 {
		return "No " + label + " recorded" + winSuffix(win) + " yet — these are parsed from the user agent at ingest, so they appear as visitors arrive."
	}
	parts := make([]string, len(rows))
	for i, r := range rows {
		parts[i] = fmt.Sprintf("%s (%d)", r.v, r.n)
	}
	// "by visitors": the numbers are first-touch visitor counts (the dashboard's unit),
	// not pageviews — say so, so the unit can't be misread.
	return "Top " + label + " by visitors" + winSuffix(win) + ": " + strings.Join(parts, ", ") + "."
}

// answerLive answers "who's on the site right now" from the last-5-minute window,
// the same live count the dashboard header shows.
func answerLive(evs []event.Event, now time.Time) string {
	cutoff := now.Add(-5 * time.Minute)
	live := map[string]bool{}
	for _, e := range evs {
		if e.Name == "$pageview" && !e.Timestamp.Before(cutoff) {
			live[e.DistinctID] = true
		}
	}
	if len(live) == 0 {
		return "Nobody on the site in the last 5 minutes."
	}
	if len(live) == 1 {
		return "1 visitor on the site right now (active in the last 5 minutes)."
	}
	return fmt.Sprintf("%d visitors on the site right now (active in the last 5 minutes).", len(live))
}

func answerWeb(evs []event.Event, win askWindow) string {
	visitors := map[string]bool{}
	views := 0
	for _, e := range evs {
		if e.Name != "$pageview" {
			continue
		}
		views++
		if e.DistinctID != "" {
			visitors[e.DistinctID] = true
		}
	}
	if views == 0 {
		// autocapture uses "$pageview", but some products track a CUSTOM event literally
		// named "pageview"/"page_view". Count that rather than falsely reporting "No
		// pageviews" while 20 such events sit in the log.
		for _, name := range []string{"pageview", "page_view"} {
			cv, cu := 0, map[string]bool{}
			for _, e := range evs {
				if e.Name == name {
					cv++
					cu[e.DistinctID] = true
				}
			}
			if cv > 0 {
				return fmt.Sprintf("%d %q events from %d users%s. (These are a custom %q event, not autocaptured $pageview.)", cv, name, len(cu), winSuffix(win), name)
			}
		}
		if win.scoped() {
			return "No pageviews " + win.label + ". The browser snippet autocaptures $pageview events — widen the window, or drop the time phrase for all history."
		}
		return "No pageviews recorded yet. The browser snippet autocaptures $pageview events — check it's installed on your site."
	}
	return fmt.Sprintf("%d pageviews from %d visitors%s.", views, len(visitors), winSuffix(win))
}

// answerTopPages ranks the most-viewed paths from $pageview events — the answer for
// "top pages" / "most visited", which used to deflect to the capabilities menu.
func answerTopPages(evs []event.Event, win askWindow) string {
	views := map[string]int{}
	for _, e := range evs {
		if e.Name != "$pageview" {
			continue
		}
		p, _ := e.Properties["path"].(string)
		if p == "" {
			p = "(no path)"
		}
		views[normPath(p)]++
	}
	if len(views) == 0 {
		if win.scoped() {
			return "No pageviews " + win.label + " to rank. Widen the window, or drop the time phrase for all history."
		}
		return "No pageviews recorded yet to rank. The browser snippet autocaptures $pageview events."
	}
	type pageRow struct {
		path string
		n    int
	}
	rows := make([]pageRow, 0, len(views))
	for p, n := range views {
		rows = append(rows, pageRow{p, n})
	}
	sort.Slice(rows, func(i, j int) bool {
		if rows[i].n != rows[j].n {
			return rows[i].n > rows[j].n
		}
		return rows[i].path < rows[j].path
	})
	parts := []string{}
	for i, r := range rows {
		if i >= 5 {
			break
		}
		parts = append(parts, fmt.Sprintf("%s (%d)", r.path, r.n))
	}
	return fmt.Sprintf("Top pages by pageviews%s: %s.", winSuffix(win), strings.Join(parts, ", "))
}

// extractPath pulls a URL path token ("/pricing") out of a question, so "visitors to
// /pricing?" resolves to /pricing. Returns "" when no path is mentioned.
func extractPath(q string) string {
	for _, w := range strings.Fields(q) {
		if strings.HasPrefix(w, "/") && len(w) > 1 {
			return strings.TrimRight(w, "?.,!\"')")
		}
	}
	return ""
}

// normPath lower-cases and strips a trailing slash so "/Pricing" and "/pricing/"
// both match the stored "/pricing".
func normPath(p string) string {
	p = strings.ToLower(strings.TrimSpace(p))
	if len(p) > 1 {
		p = strings.TrimRight(p, "/")
	}
	return p
}

// namedEvent returns the first (highest-volume) real event name the question mentions
// as a whole token. Skips $-prefixed internal events (those are pages, handled by path).
func namedEvent(q string, vol []string) string {
	toks := map[string]bool{}
	for _, w := range strings.Fields(q) {
		toks[strings.Trim(w, "?.,!\"'()")] = true
	}
	for _, ev := range vol {
		if strings.HasPrefix(ev, "$") {
			continue
		}
		if toks[strings.ToLower(ev)] {
			return ev
		}
	}
	// autocapture synonyms: "clicks" means $click (nobody types the dollar sign)
	if hasAny(q, "clicks", "click count", "how many click") {
		for _, ev := range vol {
			if ev == "$click" {
				return ev
			}
		}
	}
	// purchase synonyms map to a real purchase-shaped event so "how many purchases"
	// answers about checkout/order/payment instead of silently falling to signups
	if hasAny(q, "purchase", "bought", "buy", "order", "sale", "revenue event", "payment") {
		for _, want := range []string{"checkout", "purchase", "order", "payment", "buy", "sale"} {
			for _, ev := range vol {
				if strings.ToLower(ev) == want {
					return ev
				}
			}
		}
	}
	return ""
}

// unknownNamedEvent returns an event-shaped token the user explicitly asked about
// ("the flibbergibbet event", "how many zorptastic events fired") that is NOT in the
// dataset — so the caller can answer honestly instead of substituting a default event.
// It only fires on the explicit "<token> event(s)" pattern to avoid bouncing generic
// asks ("how many signups"): the word "event"/"events" must sit next to the token, and
// the token must not itself be a known event (that case is answerEvent's) or a generic
// metric/filler word.
func unknownNamedEvent(q string, vol []string) string {
	known := map[string]bool{}
	for _, ev := range vol {
		known[strings.ToLower(ev)] = true
	}
	words := strings.Fields(q)
	for i, w := range words {
		bare := strings.ToLower(strings.Trim(w, "?.,!\"'()"))
		if bare != "event" && bare != "events" {
			continue
		}
		// the candidate is the neighbouring noun: "the X event" (before) or
		// "X events fire" (also before). Fall back to the word after when the word
		// before is filler ("the", "an").
		var cand string
		if i > 0 {
			cand = strings.ToLower(strings.Trim(words[i-1], "?.,!\"'()"))
		}
		if (cand == "" || isFillerWord(cand)) && i+1 < len(words) {
			cand = strings.ToLower(strings.Trim(words[i+1], "?.,!\"'()"))
		}
		if cand == "" || isFillerWord(cand) || known[cand] || cand == "event" || cand == "events" {
			continue
		}
		return cand
	}
	return ""
}

// synonymNote returns a one-line disclosure when the event was resolved through a
// synonym (the tracked name does NOT appear literally in the question), so a
// substituted answer is never presented as if the user's exact word was the event.
// Empty when the user typed the real event name (or its plural) — no note needed.
func synonymNote(q, ev string) string {
	low := strings.ToLower(ev)
	if strings.Contains(q, low) || strings.Contains(q, low+"s") {
		return ""
	}
	return fmt.Sprintf("You don't track that exact event, so here's the closest one you do — %q:", ev)
}

// countNounRe pulls the noun out of the common count phrasings so an unknown one can
// be caught: "how many <noun>", "number of <noun>", "<noun> count".
var countHowManyRe = regexp.MustCompile(`how many ([a-z][a-z-]{2,})`)
var countNumberOfRe = regexp.MustCompile(`number of ([a-z][a-z-]{2,})`)

// webCountNouns are the count-nouns that belong to OTHER intents (web/active/etc), so
// the unknown-event miss must not fire on them.
var webCountNouns = map[string]bool{
	"visitor": true, "visitors": true, "visit": true, "visits": true, "view": true,
	"views": true, "pageview": true, "pageviews": true, "people": true, "person": true,
	"folks": true, "user": true, "users": true, "session": true, "sessions": true,
	"click": true, "clicks": true, "hit": true, "hits": true, "event": true, "events": true,
	"country": true, "countries": true, "day": true, "days": true, "hour": true, "hours": true,
	"time": true, "times": true, "page": true, "pages": true, "referrer": true, "referrers": true,
	"source": true, "sources": true, "signup": true, "signups": true,
}

// unknownCountNoun returns a count-noun the user asked to COUNT that is not a tracked
// event, not a web/metric term, and not filler — so the caller can miss honestly
// instead of substituting the signups/how-many catch-all. Empty when the noun is
// known or the question isn't a bare count of one noun.
func unknownCountNoun(qe string, vol []string) string {
	known := map[string]bool{}
	for _, ev := range vol {
		known[strings.ToLower(ev)] = true
	}
	var cand string
	if m := countHowManyRe.FindStringSubmatch(qe); m != nil {
		cand = m[1]
	} else if m := countNumberOfRe.FindStringSubmatch(qe); m != nil {
		cand = m[1]
	}
	if cand == "" {
		return ""
	}
	sing := strings.TrimSuffix(cand, "s")
	if isFillerWord(cand) || webCountNouns[cand] || webCountNouns[sing] ||
		known[cand] || known[sing] || namedEvent(cand, vol) != "" {
		return ""
	}
	return cand
}

// isFillerWord skips articles/generic nouns so "the ... event" resolves to the real
// name token, not to "the" or "many".
func isFillerWord(w string) bool {
	switch w {
	case "the", "a", "an", "any", "each", "this", "that", "my", "your", "which",
		"what", "how", "many", "much", "number", "count", "total", "of", "did",
		"does", "do", "fire", "fired", "fires", "named", "called", "custom":
		return true
	}
	return false
}

// answerUnknownEvent tells the user the named event doesn't exist and points at the
// nearest real name (and the discovery chips) rather than answering a substitute.
func answerUnknownEvent(name string, vol []string) string {
	real := make([]string, 0, len(vol))
	for _, ev := range vol {
		if !strings.HasPrefix(ev, "$") {
			real = append(real, ev)
		}
	}
	if len(real) == 0 {
		return fmt.Sprintf("No event named %q — in fact no product events are recorded yet. Send some with track(), then ask again.", name)
	}
	if near := nearestEvent(name, real); near != "" {
		return fmt.Sprintf("No event named %q. Did you mean %q? Your tracked events: %s.", name, near, strings.Join(cap8(real), ", "))
	}
	return fmt.Sprintf("No event named %q. Your tracked events: %s. Ask about one of those.", name, strings.Join(cap8(real), ", "))
}

// cap8 trims a listing to eight names so the answer stays one line.
func cap8(names []string) []string {
	if len(names) > 8 {
		return names[:8]
	}
	return names
}

// nearestEvent returns the closest real event name to the mistyped one by edit
// distance, but only when it's a plausible typo (distance ≤ 40% of the name) — a
// wild miss ("flibbergibbet") gets no misleading suggestion.
func nearestEvent(name string, real []string) string {
	best, bestD := "", 1<<30
	for _, ev := range real {
		d := levenshtein(name, strings.ToLower(ev))
		if d < bestD {
			best, bestD = ev, d
		}
	}
	if best == "" {
		return ""
	}
	limit := len(name) * 2 / 5
	if limit < 1 {
		limit = 1
	}
	if bestD <= limit {
		return best
	}
	return ""
}

// levenshtein is the standard edit distance, small strings only (event names), so a
// simple two-row DP is plenty.
func levenshtein(a, b string) int {
	ra, rb := []rune(a), []rune(b)
	prev := make([]int, len(rb)+1)
	cur := make([]int, len(rb)+1)
	for j := range prev {
		prev[j] = j
	}
	for i := 1; i <= len(ra); i++ {
		cur[0] = i
		for j := 1; j <= len(rb); j++ {
			cost := 1
			if ra[i-1] == rb[j-1] {
				cost = 0
			}
			cur[j] = min3(cur[j-1]+1, prev[j]+1, prev[j-1]+cost)
		}
		prev, cur = cur, prev
	}
	return prev[len(rb)]
}

func min3(a, b, c int) int {
	if b < a {
		a = b
	}
	if c < a {
		a = c
	}
	return a
}

// countish reports whether the question is asking for a count/volume, so a bare
// event mention ("how is checkout doing") still falls through to the funnel intent.
func countish(q string) bool {
	return hasAny(q, "how many", "how much", "count", "number of", "total", "mau", "dau", "wau",
		"monthly active", "daily active", "weekly active")
}

// winSuffix renders " <label>" for a scoped window, "" otherwise.
func winSuffix(w askWindow) string {
	if w.scoped() && w.label != "" {
		return " " + w.label
	}
	return ""
}

func hasAny(s string, subs ...string) bool {
	for _, sub := range subs {
		if strings.Contains(s, sub) {
			return true
		}
	}
	return false
}

// answerGeo counts distinct visitor countries (ingest-time geo). Geo stamping
// began when the operator's instance first ran a geo-enabled build and IPs are
// never stored, so history before that carries no country — the answer SAYS so
// whenever the asked window predates the first stamped event, instead of letting
// an honest small number read as a broken one.
func answerGeo(scoped, all []event.Event, win askWindow) string {
	type cAgg struct {
		visitors map[string]bool
		views    int
	}
	byCC := map[string]*cAgg{}
	unknownV := map[string]bool{} // geo ran but the IP resolved to ZZ/XX (proxy/anon) — never a real country
	for _, e := range scoped {
		if e.Name != "$pageview" {
			continue
		}
		cc, _ := e.Properties["country"].(string)
		if cc == "" {
			continue
		}
		if cc == "ZZ" || cc == "XX" {
			unknownV[e.DistinctID] = true // count as unresolved, never render "ZZ" as a country (the dashboard hides it as Unknown — the ask bar must agree)
			continue
		}
		a := byCC[cc]
		if a == nil {
			a = &cAgg{visitors: map[string]bool{}}
			byCC[cc] = a
		}
		a.views++
		a.visitors[e.DistinctID] = true
	}
	// the honesty clause: when did geo stamping actually begin on this instance?
	var firstGeo time.Time
	for _, e := range all {
		if _, ok := e.Properties["country"].(string); ok {
			if firstGeo.IsZero() || e.Timestamp.Before(firstGeo) {
				firstGeo = e.Timestamp
			}
		}
	}
	note := ""
	if !firstGeo.IsZero() && (!win.scoped() || win.from.Before(firstGeo)) {
		note = " Geo stamping began " + firstGeo.Format("Jan 2") + " on this instance (the IP is used for one lookup at ingest and never stored), so earlier events carry no country."
	}
	if len(byCC) == 0 {
		if firstGeo.IsZero() {
			return "No country data yet. Geo stamps at ingest from the free DB-IP database, so countries appear as new visitors arrive (IPs are never stored). If this instance runs with SMOLANALYTICS_GEO=off, that is why."
		}
		if len(unknownV) > 0 {
			// geo ran but resolved nothing to a real country — say so honestly instead
			// of reporting "ZZ", exactly as the dashboard hides an all-Unknown geo card
			return fmt.Sprintf("Geo ran but couldn't resolve any visitor's country — %d visitor(s) came from anonymized/proxy IPs that map to no country (shown as Unknown).%s", len(unknownV), note)
		}
		if win.scoped() {
			return "No geo-stamped pageviews " + win.label + "." + note
		}
		return "No geo-stamped pageviews yet." + note
	}
	type row struct {
		cc       string
		visitors int
	}
	rows := make([]row, 0, len(byCC))
	for cc, a := range byCC {
		rows = append(rows, row{cc, len(a.visitors)})
	}
	sort.Slice(rows, func(i, j int) bool { return rows[i].visitors > rows[j].visitors })
	parts := make([]string, 0, 5)
	for i, r := range rows {
		if i == 5 {
			break
		}
		parts = append(parts, fmt.Sprintf("%s (%d)", r.cc, r.visitors))
	}
	scope := "across all recorded geo data"
	if win.scoped() {
		scope = win.label
	}
	more := ""
	if len(rows) > 5 {
		more = fmt.Sprintf(" and %d more", len(rows)-5)
	}
	noun := "countries"
	if len(rows) == 1 {
		noun = "country"
	}
	unknownNote := ""
	if len(unknownV) > 0 {
		unknownNote = fmt.Sprintf(" (%d more from unresolved/proxy IPs, shown as Unknown)", len(unknownV))
	}
	return fmt.Sprintf("Visitors came from %d %s %s: %s%s.%s%s", len(rows), noun, scope, strings.Join(parts, ", "), more, unknownNote, note)
}
