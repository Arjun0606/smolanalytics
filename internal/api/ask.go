// briefDays maps the ask's phrasing to the brief's pulse width: "today" → 1,
// month phrasing → 30, else the weekly 7. brief.Build derives the
// current-vs-prior comparison windows from this.
func briefDays(q string) int {
	switch {
	case strings.Contains(q, "today"):
		return 1
	case strings.Contains(q, "month"):
		return 30
	default:
		return 7
	}
}

// answerBrief renders the SAME computation as `smolanalytics brief` (pulse +
// deltas + the verdict engine's findings) tight enough for the ask panel, one
// Brief struct feeds both, so the ask bar and the morning digest can never disagree.
func answerBrief(evs []event.Event, days int, now time.Time) string {
	b := brief.Build(evs, days, now)
	var s strings.Builder
	// b.Visitors counts distinct users of ANY event (active people), NOT web visitors
	// (pageviews) — label it as such so a 0-pageview site can't read "1 visitor this week"
	// and contradict the pageview-based web report.
	fmt.Fprintf(&s, "Last %d days: %d active people · %d events", b.Days, b.Visitors, b.Events)
	if b.PriorEvents == 0 {
		s.WriteString(" (no prior window to compare).")
	} else {
		people := "new"
		if b.PriorVisitors > 0 {
			people = fmt.Sprintf("%+d%%", pctChange(b.Visitors, b.PriorVisitors))
		}
		fmt.Fprintf(&s, " (vs prior %d days: active people %s, events %+d%%).", b.Days,
			people, pctChange(b.Events, b.PriorEvents))
	}
	if len(b.Findings) == 0 {
		s.WriteString(" Nothing notable, no big swings, funnel leaks, or retention flags.")
		return s.String()
	}
	s.WriteString("\nWhat to look at:")
	for _, f := range b.Findings {
		mark := "•"
		if f.Severity == "warn" {
			mark = "⚠"
		}
		fmt.Fprintf(&s, "\n%s %s, %s", mark, f.Title, f.Detail)
	}
	return s.String()
}

// pctChange mirrors brief's signed delta, direction must be unmissable.
func pctChange(cur, prior int) int {
	return int(math.Round(float64(cur-prior) / float64(prior) * 100))
}

// askWindow is an honest time scope parsed from the question. Zero from/to =
// all recorded history (the ask bar's long-standing default). The label is
// woven into every scoped answer so "this week" can never silently mean
// something else.
type askWindow struct {
	from, to time.Time
	label    string
}

func (w askWindow) scoped() bool { return !w.from.IsZero() || !w.to.IsZero() }

// windowClause weaves the computed window into a sentence: " this week (since
// Mon Jun 22, UTC)", or "" for all-time.
func windowClause(w askWindow) string {
	if !w.scoped() {
		return ""
	}
	return " " + w.label
}

var lastNDaysRe = regexp.MustCompile(`(?:last|past)\s+(\d+)\s+days?`)
var lastNHoursRe = regexp.MustCompile(`(?:last|past)\s+(\d+)\s+hours?`)
var lastNMinutesRe = regexp.MustCompile(`(?:last|past)\s+(\d+)\s+min(?:ute)?s?`)

// parseWindow maps time phrases to real windows (UTC, like every computation
// here). Recognized: today, yesterday, this/last week (calendar, Monday start),
// this/last month (calendar), and "last/past N days" (rolling, consistent with
// the engine). Anything else time-shaped (quarters, years, named months) comes
// back as unsupported so the caller can SAY so instead of silently answering
// over a different range.
func parseWindow(q string, now time.Time) (win askWindow, unsupported string) {
	const day = 24 * time.Hour
	today := now.Truncate(day)
	switch {
	case strings.Contains(q, "yesterday"):
		return askWindow{from: today.Add(-day), to: today, label: "yesterday (UTC)"}, ""
	case strings.Contains(q, "today"):
		return askWindow{from: today, to: now, label: "today (UTC)"}, ""
	case strings.Contains(q, "this week"):
		monday := today.AddDate(0, 0, -mondayOffset(today))
		return askWindow{from: monday, to: now,
			label: "this week (since Mon " + monday.Format("Jan 2") + ", UTC)"}, ""
	case strings.Contains(q, "last week"):
		monday := today.AddDate(0, 0, -mondayOffset(today))
		from := monday.AddDate(0, 0, -7)
		return askWindow{from: from, to: monday,
			label: "last week (Mon " + from.Format("Jan 2") + " – Sun " + monday.AddDate(0, 0, -1).Format("Jan 2") + ", UTC)"}, ""
	case strings.Contains(q, "past week"), strings.Contains(q, "previous week"):
		// calendar-day aligned (7 whole days ending today), identical to /v1/trends days=7
		// and the MCP tool, so every surface reports the same "last 7 days" number
		return askWindow{from: today.AddDate(0, 0, -6), to: now, label: "the last 7 days (UTC)"}, ""
	case strings.Contains(q, "past month"), strings.Contains(q, "previous month"):
		return askWindow{from: today.AddDate(0, 0, -29), to: now, label: "the last 30 days (UTC)"}, ""
	case strings.Contains(q, "this month"):
		first := time.Date(now.Year(), now.Month(), 1, 0, 0, 0, 0, time.UTC)
		return askWindow{from: first, to: now,
			label: "this month (since " + first.Format("Jan 2") + ", UTC)"}, ""
	case strings.Contains(q, "last month"):
		first := time.Date(now.Year(), now.Month(), 1, 0, 0, 0, 0, time.UTC)
		return askWindow{from: first.AddDate(0, -1, 0), to: first,
			label: "last month (" + first.AddDate(0, -1, 0).Format("January 2006") + ", UTC)"}, ""
	}
	if m := lastNMinutesRe.FindStringSubmatch(q); m != nil {
		// minute windows: "last 30 minutes" used to fall through to the empty (all-time)
		// window and answer the whole dataset under the "cannot be fabricated" stamp.
		if n, err := strconv.Atoi(m[1]); err == nil && n > 0 && n <= 60*24*366 {
			return askWindow{from: now.Add(-time.Duration(n) * time.Minute), to: now,
				label: fmt.Sprintf("the last %d minutes (UTC)", n)}, ""
		}
	}
	if m := lastNHoursRe.FindStringSubmatch(q); m != nil {
		if n, err := strconv.Atoi(m[1]); err == nil && n > 0 && n <= 24*366 {
			return askWindow{from: now.Add(-time.Duration(n) * time.Hour), to: now,
				label: fmt.Sprintf("the last %d hours (UTC)", n)}, ""
		}
	}
	if strings.Contains(q, "last hour") || strings.Contains(q, "past hour") {
		return askWindow{from: now.Add(-time.Hour), to: now, label: "the last hour (UTC)"}, ""
	}
	if m := lastNDaysRe.FindStringSubmatch(q); m != nil {
		if n, err := strconv.Atoi(m[1]); err == nil && n > 0 {
			// N whole calendar days ending today (from = midnight, n-1 days back), the
			// same window /v1/trends?days=N and the MCP trends tool use — so "last 7 days"
			// is one number across ask, API, MCP, and the dashboard tile.
			return askWindow{from: today.AddDate(0, 0, -(n - 1)), to: now,
				label: fmt.Sprintf("the last %d days (UTC)", n)}, ""
		}
	}
	if tok := unsupportedTimePhrase(q); tok != "" {
		return askWindow{}, tok
	}
	return askWindow{}, ""
}

// mondayOffset is days since the most recent Monday (0 on a Monday), Go's
// time.Weekday puts Sunday at 0, one off from a Monday-start week.
func mondayOffset(t time.Time) int { return (int(t.Weekday()) + 6) % 7 }

// unsupportedTimePhrase returns the first time-shaped token the parser does NOT
// support, so the answer can name it instead of quietly widening the window.
func unsupportedTimePhrase(q string) string {
	for _, tok := range []string{"quarter", "year", "month", "week", "hour", "minute",
		"q1", "q2", "q3", "q4", "january", "february", "march", "april", "june", "july",
		"august", "september", "october", "november", "december",
		"monday", "tuesday", "wednesday", "thursday", "friday", "saturday", "sunday"} {
		if containsWord(q, tok) {
			return tok
		}
	}
	// "since <x>" and "best/worst day" are scope-shaped phrasings the parser doesn't
	// support — refuse them by name instead of silently answering the 30-day total.
	if strings.Contains(q, "since ") {
		return "since"
	}
	return ""
}

// containsWord reports whether tok appears as a whole word, "month" must not
// fire on "monthly", or every trend question would bounce as an unsupported window.
func containsWord(q, tok string) bool {
	isSep := func(r rune) bool { return !('a' <= r && r <= 'z' || '0' <= r && r <= '9') }
	for _, w := range strings.FieldsFunc(q, isSep) {
		if w == tok {
			return true
		}
	}
	return false
}

// scope keeps events inside [from, to), the end is exclusive so "yesterday"
// and "last week" never double-count the boundary instant.
func scope(evs []event.Event, w askWindow) []event.Event {
	if !w.scoped() {
		return evs
	}
	out := make([]event.Event, 0, len(evs))
	for _, e := range evs {
		if !w.from.IsZero() && e.Timestamp.Before(w.from) {
			continue
		}
		if !w.to.IsZero() && !e.Timestamp.Before(w.to) {
			continue
		}
		out = append(out, e)
	}
	return out
}

func answerFunnel(evs []event.Event, vol []string, win askWindow, q string) string {
	fsteps, ftitle := detectFunnel(evs, vol)
	// if the question explicitly NAMES the funnel steps ("what percent of landing users
	// convert"), honor exactly those in order — never the volume-detected funnel that
	// prepends the top event (pageview). Prepending a disjoint entry step (landing users who
	// never did pageview) zeroes the named step and falsely answers "No landing users".
	if named := namedFunnelSteps(q, vol); len(named) >= 2 {
		fsteps = named
		names := make([]string, len(named))
		for i, s := range named {
			names[i] = s.Event
		}
		ftitle = strings.Join(names, " → ")
	}
	fr := funnel.Compute(evs, fsteps, 7*24*time.Hour)
	if len(fr.Steps) == 0 || fr.Steps[0].Count == 0 {
		if win.scoped() {
			return "No events " + win.label + " to build a funnel from, widen the window, or drop the time phrase for all history."
		}
		return "No events yet to build a funnel from."
	}
	if len(fr.Steps) < 2 {
		// a single distinct event name means there's no funnel to measure yet, a bare
		// "signup → signup" would print an empty drop-off step with a -1 count. Answer
		// honestly and name the next action instead.
		return fmt.Sprintf("%d users did %q%s, that's the only event type tracked, so there's no funnel to measure yet. Add a later step (activate, checkout) to see where users drop off.",
			fr.Steps[0].Count, fr.Steps[0].Event, windowClause(win))
	}
	// step-pair sub-metrics: "signup to activation rate", "how many dropped between
	// activate and checkout" — answer the exact pair, not the whole-funnel headline
	if named := stepsInQuestion(q, fr); len(named) == 2 && named[0] < named[1] {
		a, b := fr.Steps[named[0]], fr.Steps[named[1]]
		if a.Count == 0 {
			return fmt.Sprintf("No %q users%s to compute a rate from.", a.Event, windowClause(win))
		}
		rate := int(float64(b.Count)/float64(a.Count)*100 + 0.5)
		if hasAny(q, "drop", "dropped", "fall off", "fell off", "lost", "lose") {
			return fmt.Sprintf("%d users dropped between %q and %q%s (%d → %d, %d%% continue).",
				a.Count-b.Count, a.Event, b.Event, windowClause(win), a.Count, b.Count, rate)
		}
		return fmt.Sprintf("%d%% of %q users go on to %q%s (%d of %d).",
			rate, a.Event, b.Event, windowClause(win), b.Count, a.Count)
	}
	// completers: "made it all the way through", "what percent of signups end up checking out"
	if hasAny(q, "all the way", "complete the funnel", "made it through", "finish the funnel",
		"end up", "full funnel", "whole funnel", "every step") {
		last := fr.Steps[len(fr.Steps)-1]
		return fmt.Sprintf("%d users completed the full funnel %s%s — %d%% of the %d who started.",
			last.Count, ftitle, windowClause(win), pct(fr.OverallConversion), fr.Steps[0].Count)
	}
	worst, worstDrop := "", -1
	for _, st := range fr.Steps[1:] {
		if st.DroppedFromPrev > worstDrop {
			worstDrop, worst = st.DroppedFromPrev, st.Event
		}
	}
	timing := ""
	if fr.Converted > 0 && fr.MedianConvSecs > 0 {
		timing = fmt.Sprintf(" Time to convert the full funnel: median %s, p90 %s (your slowest tenth take this long — a nudge/reminder targets them).",
			humanDuration(fr.MedianConvSecs), humanDuration(fr.P90ConvSecs))
	}
	return fmt.Sprintf("%d of %d users (%d%%) complete %s%s. The biggest drop-off is at \"%s\", %d users fall off there.%s",
		fr.Steps[len(fr.Steps)-1].Count, fr.Steps[0].Count, pct(fr.OverallConversion), ftitle, windowClause(win), worst, worstDrop, timing)
}

// humanDuration renders a time-to-convert like "45s", "12m", "3h 20m", or "2.5 days".
func humanDuration(secs float64) string {
	d := time.Duration(secs) * time.Second
	switch {
	case d < time.Minute:
		return fmt.Sprintf("%ds", int(d.Seconds()))
	case d < time.Hour:
		return fmt.Sprintf("%dm", int(d.Minutes()))
	case d < 24*time.Hour:
		h := int(d.Hours())
		if m := int(d.Minutes()) % 60; m != 0 {
			return fmt.Sprintf("%dh %dm", h, m)
		}
		return fmt.Sprintf("%dh", h)
	default:
		return fmt.Sprintf("%.1f days", d.Hours()/24)
	}
}

func answerRetention(evs []event.Event, vol []string, win askWindow, now time.Time, q string) string {

	// weekly/monthly/rolling asks take the bucketed path; the default daily path below is
	// kept byte-identical so nothing about the common case changes.
	if hasAny(q, "weekly", "week", "monthly", "month", "rolling", "unbounded") {
		return answerRetentionBucketed(evs, vol, win, now, q)
	}
	// anchor: ANY event ("" = general activity) by default — the same default /v1/retention
	// and the MCP tool use. But a question that NAMES an event ("retention for open") must
	// scope to it, matching GET/MCP retention?event=open — the named event used to be
	// silently ignored, answering a different question than the one asked.
	anchor := namedEvent(normalizeEventWords(q), vol)
	// "day 30 retention" widens the horizon to the asked day and answers honestly when
	// no cohort is old enough to observe it.
	askDay := retentionPeriodAsked(q)
	maxD := 7
	if askDay > maxD {
		maxD = askDay
	}
	rr := retention.Compute(evs, maxD, anchor)
	if askDay > 7 {
		dN, sizeN := retention.DayN(rr, askDay, now)
		if sizeN == 0 {
			return fmt.Sprintf("Not enough history to measure day-%d retention yet — no cohort is %d days old. The oldest data supports day-%d at most.", askDay, askDay, oldestObservableDay(rr, now))
		}
		return fmt.Sprintf("Day-%d retention is %d%% (of %d users old enough to measure, any activity counts as returning).",
			askDay, int(float64(dN)/float64(sizeN)*100+0.5), sizeN)
	}
	// honest denominators: only cohorts old enough to observe day N (retention.DayN)
	d1, size1 := retention.DayN(rr, 1, now)
	if size1 == 0 {
		if win.scoped() {
			return "Not enough history " + win.label + " to measure retention, day-1 retention needs users past " +
				"their first day. Widen the window, or drop the time phrase for all history."
		}
		return "Not enough history yet to measure retention, check back once users are past their first day."
	}
	out := fmt.Sprintf("Day-1 retention is %d%% (of %d users past day 1).",
		int(float64(d1)/float64(size1)*100+0.5), size1)
	if d7, size7 := retention.DayN(rr, 7, now); size7 > 0 {
		out = fmt.Sprintf("Day-1 retention is %d%% and day-7 is %d%% (of %d and %d users old enough to measure).",
			int(float64(d1)/float64(size1)*100+0.5), int(float64(d7)/float64(size7)*100+0.5), size1, size7)
	}
	if win.scoped() {
		out += " Cohorts scoped to first activity " + win.label + "."
	}
	return out
}

// answerRetentionBucketed handles weekly/monthly/rolling retention asks: same honest
// denominator (only cohorts old enough to observe period N), reported in the unit asked for.
func answerRetentionBucketed(evs []event.Event, vol []string, win askWindow, now time.Time, q string) string {
	bucket, unit, p1, p2, maxP := "day", "day", 1, 7, 7
	if hasAny(q, "weekly", "week") {
		bucket, unit, p1, p2, maxP = "week", "week", 1, 4, 4
	}
	if hasAny(q, "monthly", "month") {
		bucket, unit, p1, p2, maxP = "month", "month", 1, 3, 3
	}
	rolling := hasAny(q, "rolling", "unbounded")
	// same named-event scoping as the daily path: "weekly retention for open" anchors on
	// the open event, matching GET/MCP retention?event=open.
	anchor := namedEvent(normalizeEventWords(q), vol)
	rr := retention.ComputeBucketed(evs, maxP, anchor, bucket, rolling)

	r1, s1 := retention.PeriodN(rr, p1, now)
	kind := unit
	if rolling {
		kind = "rolling " + unit
	}
	if s1 == 0 {
		return fmt.Sprintf("Not enough history yet to measure %s-over-%s retention, cohorts need to be past their first %s. Check back later, or drop the bucket for daily retention.", kind, unit, unit)
	}
	pctOf := func(a, b int) int { return int(float64(a)/float64(b)*100 + 0.5) }
	out := fmt.Sprintf("%s-1 retention is %d%% (of %d users past %s 1).", title(unit), pctOf(r1, s1), s1, unit)
	if r2, s2 := retention.PeriodN(rr, p2, now); s2 > 0 {
		out = fmt.Sprintf("%s-1 retention is %d%% and %s-%d is %d%% (of %d and %d users old enough to measure).",
			title(unit), pctOf(r1, s1), unit, p2, pctOf(r2, s2), s1, s2)
	}
	if rolling {
		out += " (Rolling: counts users active on that period or any later one.)"
	}
	if win.scoped() {
		out += " Cohorts scoped to first activity " + win.label + "."
	}
	return out
}

// title upper-cases the first letter (day -> Day) for the sentence-start unit label.
func title(s string) string {
	if s == "" {
		return s
	}
	return strings.ToUpper(s[:1]) + s[1:]
}

// answerChannels answers "which channel converts best" the way goal_report does:
// first-touch attribution per user, then per-source conversion to the site's
// conversion event, NOT the funnel, which ignores sources entirely.
func answerChannels(evs []event.Event, vol []string, win askWindow) string {
	if len(evs) == 0 {
		if win.scoped() {
			return "No events " + win.label + " to attribute, widen the window, or drop the time phrase for all history."
		}
		return "No events recorded yet."
	}
	conv := pickConversion(evs, vol)

	// first-touch: a user's channel comes from their FIRST event, read from the best
	// available signal — source, else the referrer host, else utm_source, else direct.
	// Keying off a single "source" property collapsed everyone to "direct" on real
	// autocapture data (where the landing $pageview carries `referrer`, not `source`),
	// which is exactly the misleading "direct 220 users" answer this fixes.
	firstTS := map[string]time.Time{}
	firstSrc := map[string]string{}
	converted := map[string]bool{}
	anySignal := false
	for _, e := range evs {
		if t, ok := firstTS[e.DistinctID]; !ok || e.Timestamp.Before(t) {
			firstTS[e.DistinctID] = e.Timestamp
			ch := channelOf(e)
			if ch != "direct" {
				anySignal = true
			}
			firstSrc[e.DistinctID] = ch
		}
		if e.Name == conv {
			converted[e.DistinctID] = true
		}
	}
	if !anySignal {
		return "Every visitor here is untagged (no referrer, source, or utm on their first event), so there's " +
			"nothing to attribute a channel from — they'd all read as \"direct\". Tag inbound links with utm_source, " +
			"or make sure the SDK captures the referrer, and ask again."
	}
	type row struct {
		src              string
		users, converted int
	}
	bySrc := map[string]*row{}
	for id, src := range firstSrc {
		r := bySrc[src]
		if r == nil {
			r = &row{src: src}
			bySrc[src] = r
		}
		r.users++
		if converted[id] {
			r.converted++
		}
	}
	rows := make([]*row, 0, len(bySrc))
	for _, r := range bySrc {
		rows = append(rows, r)
	}
	// listing is volume-ordered (name breaks ties → deterministic run to run)
	sort.Slice(rows, func(i, j int) bool {
		if rows[i].users != rows[j].users {
			return rows[i].users > rows[j].users
		}
		return rows[i].src < rows[j].src
	})
	if len(converted) == 0 {
		// no conversions yet — just rank the channels by volume, no "best" to name
		parts := []string{}
		for i, r := range rows {
			if i >= 3 {
				break
			}
			parts = append(parts, fmt.Sprintf("%s %d users", r.src, r.users))
		}
		return fmt.Sprintf("By channel, first-touch%s: %s. No %q conversions in this data yet, so there is no honest \"best\" to rank.",
			windowClause(win), strings.Join(parts, ", "), conv)
	}
	// "best" = highest conversion rate; users break ties so a bigger sample wins
	rateOf := func(r *row) float64 { return float64(r.converted) / float64(r.users) }
	best := rows[0]
	for _, r := range rows[1:] {
		if rateOf(r) > rateOf(best) || (rateOf(r) == rateOf(best) && r.users > best.users) {
			best = r
		}
	}
	// the listing shows the top channels AND always includes the named winner, so the
	// channel we call "best" never goes missing from the evidence list beneath it.
	parts := []string{}
	shown := map[string]bool{}
	for i, r := range rows {
		if i >= 3 {
			break
		}
		parts = append(parts, fmt.Sprintf("%s %d users → %d %s (%d%%)",
			r.src, r.users, r.converted, conv, int(rateOf(r)*100+0.5)))
		shown[r.src] = true
	}
	if !shown[best.src] {
		parts = append(parts, fmt.Sprintf("%s %d users → %d %s (%d%%)",
			best.src, best.users, best.converted, conv, int(rateOf(best)*100+0.5)))
	}
	listing := fmt.Sprintf("By channel, first-touch%s: %s.", windowClause(win), strings.Join(parts, ", "))
	return fmt.Sprintf("%s converts best to \"%s\": %d of %d first-touch users (%d%%). %s",
		best.src, conv, best.converted, best.users, int(rateOf(best)*100+0.5), listing)
}

// genericSource is true for source values that carry NO acquisition signal — a platform
// tag ("web"/"app"/"mobile") or a placeholder — so they must not win over the referrer
// host. Without this, a uniform source:"web" on the first event re-collapses every
// channel into "web" and undoes first-touch attribution entirely.
func genericSource(s string) bool {
	switch strings.ToLower(strings.TrimSpace(s)) {
	case "", "web", "app", "mobile", "desktop", "direct", "unknown", "none", "server":
		return true
	}
	return false
}

// channelOf reads a visitor's acquisition channel from an event, best signal first: an
// explicit ACQUISITION source, else the referrer host, else utm_source, else direct.
// A generic platform source ("web"/"app") is skipped so it can't mask the real referrer.
func channelOf(e event.Event) string {
	if s, _ := e.Properties["source"].(string); s != "" && !genericSource(s) {
		return s
	}
	if r, _ := e.Properties["referrer"].(string); r != "" {
		if h := hostOf(r); h != "" {
			return h
		}
	}
	if u, _ := e.Properties["utm_source"].(string); u != "" {
		return u
	}
	return "direct"
}

// pickConversion picks the event "converts" means for this dataset: the
// conventional conversion names when present, else the deepest step of the
// auto-detected journey funnel. The answer always names it, so the user sees
// exactly what was computed.
func pickConversion(evs []event.Event, vol []string) string {
	for _, want := range []string{"signup", "purchase", "checkout", "subscribe", "convert", "activate"} {
		if hasName(vol, want) {
			return want
		}
	}
	if steps, _ := detectFunnel(evs, vol); len(steps) > 0 {
		return steps[len(steps)-1].Event
	}
	return ""
}

// windowDays is the honest denominator for a per-day rate. len(trend.Points)
// includes the exclusive end-boundary's bucket, so it overstates the divisor by
// one and understates the rate (291 over a 7-day week read as 36/day, not 42).
// For a scoped window, count the whole days it actually spans; otherwise the
// point count is the day count.
// rateSuffix renders ", about N/day" only when the window is long enough for a
// daily rate to mean anything — "49 events yesterday, about 49/day" was noise,
// and a 6-hour window divided into days is nonsense.
func rateSuffix(total int, win askWindow, points int) string {
	d := windowDays(win, points)
	if d < 2 {
		return ""
	}
	// float rate, not truncating integer division: 4 events over 7 days is 0.57/day, which
	// floored to "about 0/day" reads as "nothing happened" for a non-zero count. Describe a
	// real-but-sub-1 rate honestly instead of flooring it to a misleading zero.
	rate := float64(total) / float64(d)
	if total > 0 && rate < 1 {
		return ", less than 1/day"
	}
	return fmt.Sprintf(", about %d/day", int(rate+0.5))
}

func windowDays(win askWindow, points int) int {
	if win.scoped() {
		d := int(win.to.Sub(win.from).Hours()/24 + 0.5)
		if d < 1 {
			d = 1
		}
		return d
	}
	if points < 1 {
		return 1
	}
	return points
}

// lastNDays renders "the last N days" with correct grammar (1 → "day") and a clean
// "today so far" for the degenerate 1-bucket case, so a day-old account never reads
// "over the last 1 days" or "over 0 days".
// allTimeSpan labels an UNSCOPED (all-history) total honestly. It must NOT borrow the
// "last N days" phrasing — that reads as a bounded window and made the same total render
// as "104 over the last 7 days" (all-time) vs "101 in the last 7 days" (actually bounded),
// two different numbers under one label. This says plainly it's the whole history.
func allTimeSpan(n int) string {
	if n <= 1 {
		return "all time"
	}
	return fmt.Sprintf("all time (%d days of history)", n)
}

func answerSignups(evs []event.Event, vol []string, win askWindow) string {
	ev := pickEvent(vol, "signup")
	if ev == "" {
		return "No events recorded yet."
	}
	tr := trends.Compute(evs, ev, win.from, win.to, false)
	days := len(tr.Points)
	if days == 0 {
		return "No events recorded yet."
	}
	if tr.Total == 0 && win.scoped() {
		return fmt.Sprintf("No \"%s\" events %s, widen the window, or drop the time phrase for all history.", ev, win.label)
	}
	if win.scoped() {
		return fmt.Sprintf("%d \"%s\" events %s%s.", tr.Total, ev, win.label, rateSuffix(tr.Total, win, days))
	}
	return fmt.Sprintf("%d \"%s\" events %s%s.", tr.Total, ev, allTimeSpan(days), rateSuffix(tr.Total, win, days))
}

func answerActive(evs []event.Event, win askWindow, now time.Time) string {
	if win.scoped() {
		users := distinctUsers(evs)
		if users == 0 {
			return "No active users " + win.label + ", widen the window, or drop the time phrase for all history."
		}
		return fmt.Sprintf("%d active users %s, across %d events.", users, win.label, len(evs))
	}
	total := distinctUsers(evs)
	cutoff := now.AddDate(0, 0, -7)
	recent := map[string]bool{}
	for _, e := range evs {
		if !e.Timestamp.Before(cutoff) { // inclusive "last 7 days", consistent with the engine
			recent[e.DistinctID] = true
		}
	}
	return fmt.Sprintf("%d total users, %d active in the last 7 days.", total, len(recent))
}

// answerEvent counts one named event over the window, the resolver for "how many
// checkout this week?" and the "your events" discovery chips. Mirrors answerSignups
// but for the exact event the user named rather than the picked default.
func answerEvent(evs []event.Event, ev string, win askWindow, q string) string {
	// "how many UNIQUE/DISTINCT users did X" wants deduped users, not the raw event count —
	// the engine computes both; answering the count for a users-phrased question overstates it.
	unique := hasAny(q, "unique user", "distinct user", "unique people", "distinct people",
		"how many users", "how many people", "users did", "people did", "users who", "people who")
	tr := trends.Compute(evs, ev, win.from, win.to, unique)
	days := len(tr.Points)
	unit := "events"
	if unique {
		unit = "distinct users"
	}
	if tr.Total == 0 {
		if win.scoped() {
			return fmt.Sprintf("No %q %s %s. Widen the window, or drop the time phrase for all history.", ev, unit, win.label)
		}
		return fmt.Sprintf("No %q %s recorded yet.", ev, unit)
	}
	if win.scoped() {
		return fmt.Sprintf("%d %q %s %s%s.", tr.Total, ev, unit, win.label, rateSuffix(tr.Total, win, days))
	}
	return fmt.Sprintf("%d %q %s %s%s.", tr.Total, ev, unit, allTimeSpan(days), rateSuffix(tr.Total, win, days))
}

// answerPage answers "visitors to /pricing" / "how many pageviews for /pqr" from
// autocaptured $pageview events. Path match is case- and trailing-slash-insensitive
// so the user never has to remember the exact casing.
func answerPage(evs []event.Event, path string, win askWindow) string {
	target := normPath(path)
	visitors := map[string]bool{}
	views := 0
	for _, e := range evs {
		if e.Name != "$pageview" {
			continue
		}
		p, _ := e.Properties["path"].(string)
		if normPath(p) != target {
			continue
		}
		views++
		if e.DistinctID != "" {
			visitors[e.DistinctID] = true
		}
	}
	if views == 0 {
		return fmt.Sprintf("No pageviews for %s%s. Double-check the path, or ask \"top pages\" to see what's tracked.", path, winSuffix(win))
	}
	return fmt.Sprintf("%s%s: %d visitors, %d pageviews.", path, winSuffix(win), len(visitors), views)
}

